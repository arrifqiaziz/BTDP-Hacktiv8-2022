public class operator2 {
    public static void main(String[] args) {
        int x = 20;
        int y = 40;

        System.out.println("x + y = " + (x+y));
        System.out.println("x - y = " + (x-y));
        System.out.println("x * y = " + (x*y));
        System.out.println("x / y = " + (x/y));
        System.out.println("x++ = " + (x++));
        System.out.println("x-- = " + (x--));

        //perbedaan x++ dan ++x
        System.out.println("x++ = " + (x++));
        System.out.println("++x = " + (++x));

    }
}
